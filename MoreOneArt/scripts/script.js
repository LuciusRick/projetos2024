function mudouTamanho(){
    if (window.innerWidth >= 768){
        itensid.style.display = 'block'
    } else {
        itensid.style.display = 'none'
    }
}

function clickMenu() {
    if (itensid.style.display == 'none'){
        itensid.style.display = 'block'
    } else {
        itensid.style.display = 'none'
    }
    
}
